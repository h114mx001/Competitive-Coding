#include <bits/stdc++.h>
using namespace std;    
int main(){
    int day, month, year;
    printf("Enter your birthday, with dd/mm/yyyy format: ");
    scanf("%d/%d/%d",&day,&month,&year);        //&x is the address of x
    printf("Your birthday is %d/%d/%d",day,month,year);

    //use %f for float and %lf for double and long double
    //whitespace is skipped if you want to input a int, double,... or any numeric data, but not if you want a character or a string
    
}